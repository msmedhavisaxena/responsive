    <h2>What is Elocute?</h2>
  
  <p>Elocute is designed for the parents of very special children- the  hearing challenged.  
Lack of language skill turns hearing loss into hearing handicap and inadequate development of language and speech has an adverse effect on educational, emotional, intellectual, personal and social development of the child.  
The skill of speaking which is effective and socially acceptable,is Elocute all about.  The childâ€™s first teacher are the parents and they play an important role in the training of a  hearing challenged child.  As a parent you have the ability to prevent hearing loss turning into " hearing handicap". Parents cannot replace the professionals, but it is important for them to undertake the role of a teacher at home, where the child spends maximum time.  The teachers and parents should work collaboratively in the early childhood as it is a critical period of language acquisition. </p></div>

<div class="col-md-4">
    <h2>Learning Process</h2>
<p>Learning language and speech is a long and difficult process.  The hearing challenged children miss on early language inputs. Unlike normal children they are not exposed to sounds, it's variables nor the language, hence it becomes all the more important to have extensive training programs at home to facilitate language development. A parent can personalise the learning plans for the child as he knows him the best.  They can provide a language stimulating environment at home as the child is familiar with everything.  
They can provide a unique environment to their child at home to maximise the child's interest in learning, give him self-confidence and prepare him for success beyond the classroom.  They can adopt different methods and situations which can be used to generate interest and develop conversation with the child in the absence of a speech therapist.   The sessions should be intensive and everyday.</p> </div> 

<div class="col-md-4">
    <h2>Strength</h2>
<p> A hearing impaired child can be a success and reach greater heights with the support of parents, professionals and community.  It is a challenge for the child and the parent both, but NOT IMPOSSIBLE.  He will make a small invisible progress every day.  The development of speaking and language skills are the basis for communication. The first meaningful word from your child can be the most satisfying and memorable day in your life.</p>
